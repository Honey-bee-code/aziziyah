                <p style="margin-top: 20px;"><strong>Muqaddimah</strong></p>
<p>Paradigma baru dunia pendidikan yang hanya minitikberatkan  pada kecerdasan intelektual (IQ) telah membawa perkembangan bangsa ini ke arah yang jauh dari nilai-nilai moral dan keagamaan. Prosentase pendidikan keagamaan yang seharusnya menjadi prioritas bangsa ini agar tumbuh menjadi bangsa yang bermoral dan bermartabat dinilai sangat tidak berkeadilan (berkisar antara 5 – 15% baik di sekolah maupun di madrasah-madrasah). Realita seperti ini tentunya tidak selaras dan kontra produktif dengan tujuan Pendidikan Nasional yang menitikberatkan pada terbentuknya potensi manusia yang beriman, bertakwa serta berakhlak mulia.</p>
<p>Berawal dari kenyataan di atas dan dengan didasari niat ikhlas untuk memberikan konstribusi terbaik bagi perkembangan generasi penerus bangsa, maka Yayasan Pondok Pesantren Al-Aziziyah melalui Lembaga Pendidikan Madrasatul Qur’an Wal Hadits (MQWH) mencoba menawarkan kurikulum pembelajaran yang terbalik dengan pembelajaran pemerintah, dalam artian Pendidikan Keagamaan menjadi prioritas utama dan yang pertama dalam proses pembelajaran.</p>
<p><strong>Eksistensi dan Status</strong></p>
<p>Madrasatul Qur’an Wal Hadits (MQWH) merupakan Lembaga Pendidikan non-formal sekaligus merupakan Lembaga Pendidikan tertua yang bernaung di bawah Yayasan  Pondok Pesantren Al-Aziziyah. Seiring dengan Program Pemerintah tentang wajardiknas (Wajib Belajar 9 tahun), Madrasatul Qur’an Wal Hadits termasuk salah satu lembaga pendidikan non-formal yang mendapat rekomendasi pemerintah untuk menyelenggarakan program tersebut melalui Surat Keputusan Bersama (SKB) dua menteri yaitu Menteri Pendidikan Nasional dan Menteri Agama RI tentang Pondok Pesantren Salafiyah (PPS).</p>
<p>Dengan demikian, MQWH Al-Aziziyah meski merupakan lembaga pendidikan non-formal akan tetapi memiliki legalitas formal yang terimplementasikan dalam program Ujian Nasional pemerintah untuk Pondok Pesantren Salafiyah. Sehingga para santri Madrasatul Qur’an Wal Hadits (MQWH), khususnya bagi santri MQWH tingkat Wustho di akhir masa studinya akan mendapatkan dua buah ijazah yaitu ijazah formal yang setara dengan ijazah SMP/MTs dan ijazah non-formal (lokal).</p>

<p><strong>Visi, Misi, dan Tujuan</strong></p>

<i>Visi:</i>
<p>“Mencetak Ulama’ Penghafal Qur’an”</p>

<i>Misi:</i>
<p>   1. Memberikan modal awal atau spritual basic yang kuat bagi peserta didik melalui program menghafal Al-Qur’an.</p>
<p>    2. Memberikan pembelajaran dan penguasaan terhadap ilmu-ilmu dasar Bahasa Arab seperti Nahwu, Shorof, Balagoh, Muthola’ah dan lain sebagainya.</p>
<p>    3. Memberikan pembelajaran dan penguasaan terhadap ilmu-ilmu keislaman lainnya seperti Aqidah/Tauhid, Fiqih, Ushul Fiqih, Tafsir, Ulumut Tafsir/Ulumul Qur’an, Hadits dan Ulumul Hadits dan lain sebagainya.</p>
<p>    4. Memberikan pembelajaran dan penguasaan dasar terhadap ilmu-ilmu umum seperti Matematika, IPA, IPS, Teknoloki (Komputer) maupun Bahasa Inggris.</p>
<i> Tujuan:</i>
<p> Mewujudkan terbentuknya generasi Qur’ani yang mampu mengintegrasikan ajaran-ajaran Islam melalui pengamalan Imtaq dan Iptek.</p>

<p><strong> Masa Pendidikan dan Waktu Belajar</strong></p>

<i> Masa Pendidikan:</i>
<p> Madrasatul Qur’an Wal Hadits (MQWH) terdiri dari dua jenjang masa pendidikan, masing-masing 3 tahun yaitu:</p>
<p>    1. Tingkat Wustho (setara dengan SMP/MTs)</p>
<p>    2. Tingkat ‘Ulya (setara dengan SMA/MA)</p>

