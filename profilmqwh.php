<p>    2. Siang (bagi santriwati)	: jam 13.15 – 17.30 (wita)</p>

<p><strong> Kurikulum Terapan, Bahan Ajar dan Mata Pelajaran</strong></p>

<i> Kurikulum dan Bahan Ajar:</i>
<p> Sebagai bentuk pengejawantahan terhadap visi, misi dan tujuan Madrasatul Quran Wal Hadits (MQWH) di atas, maka di buatlah suatu kurikum terpadu yang memadukan antara kurikulum Pondok Pesantren dan Kurikulum Wajardiknas 9 tahun yang tertuang dalam kurikulum program Pondok Pesantren Salafiyah (PPS) dengan kalkukasi 80% Ilmu-Ilmu keislaman dan Bahasa serta 20% ilmu-ilmu exact dan teknologi. Sedangkan bahan ajaran untuk ilmu-ilmu keislaman seluruhnya memakai kitab-kitab yang menggunakan teks Bahasa Arab dan tidak diperkenankan memakai teks terjemahan.</p>

